package codingAdvanced;

public class Developer extends Person {


    public Developer(String fName, String lName, String id, int age) {
        super(fName, lName, id, age);
    }


    @Override
    public String getfName() {
        return super.getfName();
    }

    @Override
    public String getlName() {
        return super.getlName();
    }

    @Override
    public String getId() {
        return super.getId();
    }

    @Override
    public int getAge() {
        return super.getAge();
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
